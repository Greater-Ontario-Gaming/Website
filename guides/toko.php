<h3>I am a LEO or EMS. How do I talk on radio with TokoVOIP?</h3>
<br>
Press Shift + F3  you will open your radio use your left arrow and right arrow keys to change to your appropriate channel
<br>
<br>
<h3>People can’t hear me!</h3>
Here are some common problems that cause this:
<br>
Do not use the N key for the base FiveM voice chat. Instead, use the push-to-talk key configured in TeamSpeak.
<br>
Do not configure your TeamSpeak push-to-talk key as Z.
<br>
Check your microphone capture device is set correctly in TeamSpeak: Tools>Options>Capture
<br>
Ensure you are not talking to people further away than your voice distance is set for. Press Z to toggle between whisper, normal, and shouting.
<br>
<br>
<h3>I can’t hear anyone</h3>
<br>
Check your playback device is set correctly in TeamSpeak: Tools>Options>Playback
